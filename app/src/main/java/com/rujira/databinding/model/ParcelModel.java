package com.rujira.databinding.model;

public class ParcelModel {
    private String parcelName;
    private int parcelType;
    private double parcelPrice;

    public String getParcelName() {
        return parcelName;
    }

    public void setParcelName(String parcelName) {
        this.parcelName = parcelName;
    }

    public int getParcelType() {
        return parcelType;
    }

    public void setParcelType(int parcelType) {
        this.parcelType = parcelType;
    }

    public double getParcelPrice() {
        return parcelPrice;
    }

    public void setParcelPrice(double parcelPrice) {
        this.parcelPrice = parcelPrice;
    }
}
